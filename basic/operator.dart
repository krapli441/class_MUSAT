void main() {
  int number = 2;
  print(number);
  print(number + 2);
  print('-------------------------');
  print(number % 2);
  print(number % 3);
  print('-------------------------');
  number++;
  print(number);
  number--;
  print(number);
  print('-------------------------');
  double doubleNumber = 2.5;
  print(doubleNumber);
  doubleNumber += 1; // 원하는 숫자만큼 더할 수 있다.
  print(doubleNumber);
}
